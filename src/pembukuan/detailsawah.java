    public void setdetailsawah(JTable tabel) {
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        String id = model.getValueAt(tabel.getSelectedRow(), 0).toString();
        iddetailsawah = id;
        String detailsawah[] = mdetailsawah.getdetailsawah(id);

        inp_nama.setText(detailsawah[1]);
        inp_keterangan.setText(detailsawah[2]);
        inp_besar.setText(detailsawah[4]);
        if (detailsawah[3].equalsIgnoreCase("keluar")) {
            inp_status.setSelectedIndex(0);
        } else {
            inp_status.setSelectedIndex(1);
        }
        btnsimpan.setText("update");
    }
    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        try {
            if (btnsimpan.getText().equalsIgnoreCase("simpan")) {
                String data[] = {
                    idsawah,
                    inp_nama.getText(),
                    inp_keterangan.getText(),
                    inp_status.getSelectedItem().toString(),
                    inp_besar.getText()
                };
                mdetailsawah.tambahdetailsawah(data);
            } else {
                String data[] = {
                    iddetailsawah,
                    inp_nama.getText(),
                    inp_keterangan.getText(),
                    inp_status.getSelectedItem().toString(),
                    inp_besar.getText()
                };
                mdetailsawah.updatedetailsawah(data);
                btnsimpan.setText("update");
            }
            clear();
            updatetabel();
        } catch (SQLException ex) {
            Logger.getLogger(detailsawah.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnsimpanActionPerformed
    public void clear() {
        try {
            inp_nama.setText("");
            inp_besar.setText("");
            inp_keterangan.setText("");
            inp_status.setSelectedIndex(0);
            btnsimpan.setText("simpan");
            updatetabel();
        } catch (SQLException ex) {
            Logger.getLogger(detailsawah.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
