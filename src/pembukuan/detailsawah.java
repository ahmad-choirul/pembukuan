    public void updatetabel() throws SQLException {
        tabelkeluar.setModel(mdetailsawah.getdana("keluar", idsawah));
        tabelmasuk.setModel(mdetailsawah.getdana("masuk", idsawah));
        hapusno(tabelmasuk);
        hapusno(tabelkeluar);
        btnhapus.setEnabled(false);
        DefaultTableModel modelkeluar = (DefaultTableModel) tabelkeluar.getModel();
        DefaultTableModel modelmasuk = (DefaultTableModel) tabelmasuk.getModel();
        double uangkeluar = 0;
        double uangmasuk = 0;

        for (int i = 0; i < modelkeluar.getRowCount(); i++) {
            double getuang = Integer.parseInt(modelkeluar.getValueAt(i, 2).toString());
            uangkeluar += getuang;
        }
        for (int i = 0; i < modelmasuk.getRowCount(); i++) {
            double getuang = Integer.parseInt(modelmasuk.getValueAt(i, 2).toString());
            uangmasuk += getuang;
        }
        txtdanakeluar.setText(uangkeluar + "");
        txtdanamasuk.setText(uangmasuk + "");
keuntungan.setText((uangmasuk-uangkeluar)+"");
    }

    public void hapusno(JTable model) {
        TableColumn kolom = model.getColumnModel().getColumn(0);
        kolom.setMinWidth(0);
        kolom.setMaxWidth(0);
        kolom.setWidth(0);
        kolom.setPreferredWidth(0);
    }

